
function redir(bttn) {
    switch (bttn)
            {
            case 'bttn_login':
                  window.location = "vistas/UserLogin.html";
                  break;
            case 'bttn_register':
                   window.location = "vistas/UserReg.html";
                   break;
            case 'bttn_Images':
               break;
      }
}


